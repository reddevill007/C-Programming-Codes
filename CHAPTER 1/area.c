#include <stdio.h>

int main()
{
    int length, breadth;
    printf("THE LENGTH OF THE RECTANGLE IS\n", length);
    scanf("%d", &length);

    printf("THE BREADTH OF THE RECTANGLE IS\n", breadth);
    scanf("%d", &breadth);

    printf("THE AREA OF THE RECTANGLE IS %d\n", length * breadth);
    return 0;
}